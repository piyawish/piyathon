import random


# สร้างเกมทายตัวเลข
def เกมทายตัวเลข():
    ตัวเลขที่ถูก = random.randint(1, 10)
    จำนวนครั้ง = 0

    print("ยินดีต้อนรับสู่เกมทายตัวเลข!")
    print("ฉันกำลังคิดถึงตัวเลขระหว่าง 1 ถึง 10")

    while True:
        คำตอบ = int(input("ลองทายดูสิ: "))
        จำนวนครั้ง += 1

        if คำตอบ < ตัวเลขที่ถูก:
            print("น้อยไป! ลองใหม่อีกครั้ง")
        elif คำตอบ > ตัวเลขที่ถูก:
            print("มากไป! ลองใหม่อีกครั้ง")
        else:
            print(f"ยินดีด้วย! คุณทายถูกใน {จำนวนครั้ง} ครั้ง")
            break


# เรียกใช้เกม
เกมทายตัวเลข()
